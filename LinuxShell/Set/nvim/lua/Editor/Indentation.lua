vim.opt.autoindent = true --依据上一行缩进新行
vim.opt.copyindent = true --使 "autoindent" 使用复制文本的缩进结构
vim.opt.shiftround = true --将缩进缩进到移位宽度的倍数
vim.opt.shiftwidth = 2 --用于(自动)缩进步长的空格数
vim.opt.expandtab = true --插入 <Tab> 时使用空格
vim.opt.smarttab = true --插入 <Tab> 时使用 "shiftwidth"
vim.opt.softtabstop = 2 --编辑 <Tab> 使用的空格数
vim.opt.tabstop = 2 --文件中<Tab>使用的空格数